#include "libft.h"

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	int i;

	if (!s1 || !s2)
		return (0);
	i = 0;
	while ((*s1 != '\0' || *s2 != '\0') && n != 0)
	{
		if (*s1 == *s2)
		{
			i++;
		}
		else
			return (0);
		n--;
		s1++;
		s2++;
	}
	return (1);
}
